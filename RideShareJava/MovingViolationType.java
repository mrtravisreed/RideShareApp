/**
 * MovingViolationType enumerates the types of moving violations.
 *
 */
public enum MovingViolationType {

  /**
   * Distracted driving.
   */
  DISTRACTED_DRIVING,

  /**
   * Reckless driving.
   */
  RECKLESS_DRIVING,

  /**
   * Speeding.
   */
  SPEEDING,

  /**
   * Driving under the influence.
   */
  DUI,

  /**
   * Failure to respect traffic signs.
   */
  TRAFFIC_SIGNS,

  /**
   * Driving without a valid license and/or insurance.
   */
  LICENSE_OR_INSURANCE
}
